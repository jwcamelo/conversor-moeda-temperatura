    private void btnConverterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConverterActionPerformed
        if(comboMoeda1.getSelectedIndex()==0 || comboMoeda2.getSelectedIndex() == 0)
            JOptionPane.showMessageDialog(null,"Selecione a moeda para conversão!","Moeda não selecionada",1);
        else
            if(comboMoeda1.getSelectedIndex() == comboMoeda2.getSelectedIndex())
                JOptionPane.showMessageDialog(null,"As moedas selecionadas são iguais!","Moedas selecionadas iguais",1);
            else
                lbValorConvertido.setText(converter(comboMoeda1.getSelectedItem().toString(),comboMoeda2.getSelectedItem().toString(),Double.parseDouble(txtValor.getText().replace(",", "."))));
            
        
    }//GEN-LAST:event_btnConverterActionPerformed

