    public String converter(String moeda1, String moeda2, Double valor){
        
        NumberFormat real = NumberFormat.getCurrencyInstance(new Locale("pt","BR"));
        NumberFormat dolar = NumberFormat.getCurrencyInstance(new Locale("en","US"));
        NumberFormat euro = NumberFormat.getCurrencyInstance(new Locale("pt","PT"));
        NumberFormat dolarCA = NumberFormat.getCurrencyInstance(new Locale("en","CA"));
        NumberFormat libra = NumberFormat.getCurrencyInstance(new Locale("en","GB"));
        
        
        if(moeda1.equals("Real")){
            if(moeda2.equals("Dólar US"))
                return real.format(valor)+" = "+dolar.format(valor*0.19);
            if(moeda2.equals("Euro"))
                return real.format(valor)+" = "+euro.format(valor*0.18);
            if(moeda2.equals("Dólar CAD"))
                return real.format(valor)+" = "+dolarCA.format(valor*0.25);
            if(moeda2.equals("Libra Esterlina"))
                return real.format(valor)+" = "+libra.format(valor*0.16); 
        }
        if(moeda1.equals("Dólar US")){
            if(moeda2.equals("Real"))
                return dolar.format(valor)+" = "+real.format(valor*5.23);
            if(moeda2.equals("Euro"))
                return dolar.format(valor)+" = "+euro.format(valor*0.94);
            if(moeda2.equals("Dólar CAD"))
                return dolar.format(valor)+" = "+dolarCA.format(valor*1.29);
            if(moeda2.equals("Libra Esterlina"))
                return dolar.format(valor)+" = "+libra.format(valor*0.81);
        }
        if(moeda1.equals("Euro")){
            if(moeda2.equals("Real"))
                return euro.format(valor) + " = "+real.format(valor*5.53);
            if(moeda2.equals("Dólar US"))
                return euro.format(valor) + " = "+dolar.format(valor*1.06);
            if(moeda2.equals("Dólar CAD"))
                return euro.format(valor) + " = "+dolarCA.format(valor*1.36);
            if(moeda2.equals("Libra Esterlina"))
                return euro.format(valor)+" = "+libra.format(valor*0.86);
        }
        if(moeda1.equals("Dólar CAD")){
            if(moeda2.equals("Real"))
                return dolarCA.format(valor) + " = "+real.format(valor*4.06);
            if(moeda2.equals("Dólar US"))
                return dolarCA.format(valor) + " = "+dolar.format(valor*0.78);
            if(moeda2.equals("Euro"))
                return dolarCA.format(valor) + " = "+euro.format(valor*0.73);
            if(moeda2.equals("Libra Esterlina"))
                return dolarCA.format(valor)+" = "+libra.format(valor*0.63);
        }
        if(moeda1.equals("Libra Esterlina")){
            if(moeda2.equals("Real"))
                return libra.format(valor) + " = "+real.format(valor*6.42);
            if(moeda2.equals("Dólar US"))
                return libra.format(valor) + " = "+dolar.format(valor*1.23);
            if(moeda2.equals("Euro"))
                return libra.format(valor) + " = "+euro.format(valor*1.16);
            if(moeda2.equals("Dólar CAD"))
                return libra.format(valor)+" = "+dolarCA.format(valor*1.58);
        }
        
        return "";       
    }

