    private void btnConverterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConverterActionPerformed
        if (comboTemp1.getSelectedIndex() == 0 || comboTemp2.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Selecione a unidade para conversão!", "Unidade de temperatura não selecionada", 1);
        } else if (comboTemp1.getSelectedIndex() == comboTemp2.getSelectedIndex()) {
            JOptionPane.showMessageDialog(null, "As unidades selecionadas são iguais!", "Moedas selecionadas iguais", 1);
        } else {
            lbValorConvertido.setText(converter(comboTemp1.getSelectedItem().toString(), comboTemp2.getSelectedItem().toString(), Double.parseDouble(txtValor.getText().replace(",", "."))));
        }


    }//GEN-LAST:event_btnConverterActionPerformed

