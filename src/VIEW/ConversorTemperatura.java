    public String converter(String temp1, String temp2, Double valor) {
        DecimalFormat decimal = new DecimalFormat("#,##0.00");

        if (temp1.equals("Celsius (°C)")) {
            if (temp2.equals("Fahrenheit (°F)")) {
                return decimal.format(valor * 9 / 5 + 32) + " °F";
            }
            if (temp2.equals("Kelvin (K)")) {
                return decimal.format(valor + 273.15) + " K";
            }
        }
        if (temp1.equals("Fahrenheit (°F)")) {
            if (temp2.equals("Celsius (°C)")) {
                return decimal.format((valor - 32) * 5 / 9) + "(°C)";
            }
            if (temp2.equals("Kelvin (K)")) {
                return decimal.format((valor - 32) * 5 / 9 + 273.15) + " K";
            }

        }
        if (temp1.equals("Kelvin (K)")) {
            if (temp2.equals("Fahrenheit (°F)")) {
                return decimal.format((valor - 273.15) * 9 / 5 + 32) + " °F";
            }
            if (temp2.equals("Celsius (°C)")) {
                return decimal.format(valor - 273.15) + "°C";
            }

        }

        return "";
    }

